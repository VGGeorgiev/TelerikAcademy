namespace PrimitiveTypesPerformance
{
    public static class AddValue
    {
        public static void AddInt(int maxValue)
        {
            int result = 0;

            while (result < maxValue)
            {
                result++;
            }
        }

        public static void AddLong(long maxValue)
        {
            long result = 0;

            while (result < maxValue)
            {
                result++;
            }
        }

        public static void AddFloat(float maxValue)
        {
            float result = 0;

            while (result < maxValue)
            {
                result++;
            }
        }

        public static void AddDouble(double maxValue)
        {
            double result = 0;

            while (result < maxValue)
            {
                result++;
            }
        }

        public static void AddDecimal(decimal maxValue)
        {
            decimal result = 0;

            while (result < maxValue)
            {
                result++;
            }
        }
    }
}
